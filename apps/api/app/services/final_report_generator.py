def _assemble_final_report_content(
    company_snapshot: dict[str, Any] | None,
    company_record: dict[str, Any] | None,
    candidate: ScreeningCandidate | None,
    scorecard: Scorecard | None,
    financial_data_summary: dict[str, Any] | None,
    source_quality_summary: dict[str, Any] | None,
    research_completeness_summary: dict[str, Any] | None,
    upgraded_citation_validation: dict[str, Any] | None,
    bull_case_summary: dict[str, Any] | None,
    bear_case_summary: dict[str, Any] | None,
    risk_summary: dict[str, Any] | None,
    valuation_guard_summary: dict[str, Any] | None,
    committee_chair_summary: dict[str, Any] | None,
    fundamentals_data: dict[str, Any] | None,
    fundamentals_available: bool | None,
    source_tier: str | None,
    sources: list[Source],
    citations: list[Citation],
    report: Report | None,
    agent_run_id: str | None,
    schema_valid: bool | None,
    human_review_required: bool | None,
) -> dict[str, Any]:
    """
    Assemble all 19 sections into a structured report dict.
    """
    company_name = None
    ticker = None

    if company_snapshot:
        ci = company_snapshot.get("company_identity", {})
        company_name = ci.get("legal_name")
        ticker = ci.get("ticker")
    elif company_record:
        company_name = company_record.get("name")
        ticker = company_record.get("ticker")
    elif candidate:
        company_name = candidate.name
        ticker = candidate.ticker

    provisional_status = None
    if committee_chair_summary:
        provisional_status = committee_chair_summary.get("provisional_internal_status")
    if scorecard and not provisional_status:
        provisional_status = scorecard.internal_status

    missing_section = _build_missing_information(
        financial_data_summary,
        research_completeness_summary,
        company_snapshot,
        candidate,
    )
    missing_count = missing_section.get("total_missing_items", 0)

    is_mock = True
    if company_snapshot:
        is_mock = company_snapshot.get("is_mock", True)
    elif scorecard:
        source_q = scorecard.source_quality_summary_json or {}
        is_mock = source_q.get("is_mock", True)

    return {
        "admin_disclaimer": _build_admin_disclaimer(),
        "executive_summary": _build_executive_summary(
            company_name,
            ticker,
            scorecard,
            committee_chair_summary,
            provisional_status,
        ),
        "company_identity": _build_company_identity(
            company_snapshot, company_record
        ),
        "discovery_rationale": _build_discovery_rationale(candidate),
        "data_availability_summary": _build_data_availability_summary(
            financial_data_summary, fundamentals_available, source_tier
        ),
        "financial_snapshot": _build_financial_snapshot(
            company_snapshot, fundamentals_data
        ),
        "internal_scorecard": _build_internal_scorecard(scorecard),
        "valuation_readiness": _build_valuation_readiness(
            valuation_guard_summary, scorecard
        ),
        "bull_case": _build_bull_case(bull_case_summary),
        "bear_case": _build_bear_case(bear_case_summary),
        "risk_analysis": _build_risk_analysis(risk_summary),
        "source_quality_review": _build_source_quality_review(
            source_quality_summary, sources
        ),
        "citation_validation_review": _build_citation_validation_review(
            upgraded_citation_validation, citations
        ),
        "research_completeness_review": _build_research_completeness_review(
            research_completeness_summary
        ),
        "missing_information": missing_section,
        "committee_chair_summary": _build_committee_chair_summary(
            committee_chair_summary
        ),
        "workflow_status": _build_workflow_status(
            report, agent_run_id, schema_valid, human_review_required
        ),
        "human_review_checklist": [
            item.model_dump()
            for item in _build_human_review_checklist(
                safety_valid=True,  # placeholder — updated after safety gate
                schema_valid=schema_valid or False,
                has_scorecard=scorecard is not None,
                has_bull_bear=bull_case_summary is not None
                and bear_case_summary is not None,
                has_risk=risk_summary is not None,
                has_citations=len(citations) > 0,
                missing_count=missing_count,
                is_mock=is_mock,
            )
        ],
        "source_citation_appendix": _build_source_citation_appendix(
            sources, citations
        ),
    }


