def _build_missing_information(
    financial_data_summary: dict[str, Any] | None,
    research_completeness_summary: dict[str, Any] | None,
    company_snapshot: dict[str, Any] | None,
    candidate: ScreeningCandidate | None,
) -> dict[str, Any]:
    missing: list[dict[str, str]] = []

    # From snapshot missing fields
    if company_snapshot:
        for field in company_snapshot.get("missing_fields", []):
            missing.append({"field": field, "source": "company_snapshot"})

    # From financial data
    if financial_data_summary:
        for field in financial_data_summary.get("missing_fields", []):
            if field not in [m["field"] for m in missing]:
                missing.append({"field": field, "source": "financial_data_agent"})

    # From research completeness
    if research_completeness_summary:
        for gap in research_completeness_summary.get("incomplete_sections", []):
            missing.append({"field": gap, "source": "research_completeness_agent"})

    # From candidate
    if candidate:
        for field in (candidate.missing_data_json or []):
            if field not in [m["field"] for m in missing]:
                missing.append({"field": field, "source": "screening_candidate"})

    return {
        "type": "missing_information",
        "total_missing_items": len(missing),
        "missing_items": {
            "value": missing,
            "provenance": "sourced_fact",
        },
        "human_review_required": len(missing) > 0,
        "note": (
            "All missing items must be resolved or explicitly acknowledged "
            "before a report can be approved internally."
        ),
    }


