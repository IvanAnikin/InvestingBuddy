def _extract_from_report_content(report: Report) -> dict[str, Any]:
    """
    Parse an existing report's stored JSON content or structured fields
    to extract the council outputs needed for final report sections.
    """
    content: dict[str, Any] = {}
    if report.content_markdown:
        # Simple heuristic extraction — look for JSON blocks in content
        import json
        import re

        pattern = re.compile(r"```json\s*(.*?)\s*```", re.DOTALL)
        for match in pattern.finditer(report.content_markdown):
            try:
                block = json.loads(match.group(1))
                if isinstance(block, dict):
                    content.update(block)
            except (json.JSONDecodeError, ValueError):
                pass

    return content


