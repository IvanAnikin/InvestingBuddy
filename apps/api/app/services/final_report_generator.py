def _build_executive_summary(
    company_name: str | None,
    ticker: str | None,
    scorecard: Scorecard | None,
    committee_chair_summary: dict[str, Any] | None,
    provisional_internal_status: str | None,
) -> dict[str, Any]:
    status = provisional_internal_status or "not_enough_data"
    if status not in ALLOWED_INTERNAL_STATUSES:
        status = "not_enough_data"

    overall_score = None
    score_note = "Scorecard not available."
    if scorecard:
        overall_score = scorecard.overall_score
        score_note = (
            f"Research attractiveness score: {overall_score}/100 "
            f"(internal queue label: {scorecard.internal_status}). "
            "Score is NOT a public recommendation."
        )

    committee_note = "Analysis council summary not available."
    if committee_chair_summary:
        committee_note = committee_chair_summary.get(
            "committee_summary", "Analysis council summary not available."
        )

    return {
        "type": "executive_summary",
        "company_name": company_name or "Unknown",
        "ticker": ticker,
        "overall_score": overall_score,
        "internal_status": status,
        "score_note": {
            "value": score_note,
            "provenance": "sourced_fact" if scorecard else "missing_data",
            "human_review_required": True,
        },
        "committee_note": {
            "value": committee_note,
            "provenance": "model_interpretation",
            "human_review_required": True,
        },
        "disclaimer": "INTERNAL ADMIN DRAFT. NOT INVESTMENT ADVICE. Human review required.",
        "human_review_required": True,
    }


def _build_company_identity(
    company_snapshot: dict[str, Any] | None,
    company_record: dict[str, Any] | None,
) -> dict[str, Any]:
    identity: dict[str, Any] = {"type": "company_identity"}

    def _dp(value: Any, source: str, provenance: str = "sourced_fact") -> dict:
        return {
            "value": value,
            "source": source,
            "provenance": provenance,
            "human_review_required": value is None,
        }

    if company_snapshot:
        ci = company_snapshot.get("company_identity", {})
        profile = company_snapshot.get("profile", {})
        identity["legal_name"] = _dp(ci.get("legal_name"), "company_snapshot")
        identity["ticker"] = _dp(ci.get("ticker"), "company_snapshot")
        identity["exchange"] = _dp(ci.get("exchange"), "company_snapshot")
        identity["country_domicile"] = _dp(
            ci.get("country_domicile"), "company_snapshot"
        )
        identity["isin"] = _dp(
            ci.get("isin"),
            "company_snapshot",
            "missing_data" if not ci.get("isin") else "sourced_fact",
        )
        identity["lei"] = _dp(
            ci.get("lei"),
            "company_snapshot",
            "missing_data" if not ci.get("lei") else "sourced_fact",
        )
        identity["sector"] = _dp(
            profile.get("sector"), "company_snapshot.profile"
        )
        identity["reporting_currency"] = _dp(
            profile.get("reporting_currency"), "company_snapshot.profile"
        )
        identity["source_tier"] = company_snapshot.get(
            "source_tier", "T6_model_estimate"
        )
        identity["is_mock"] = company_snapshot.get("is_mock", True)
    elif company_record:
        identity["legal_name"] = _dp(company_record.get("name"), "company_db_record")
        identity["ticker"] = _dp(company_record.get("ticker"), "company_db_record")
        identity["exchange"] = _dp(company_record.get("exchange"), "company_db_record")
        identity["country_domicile"] = _dp(
            company_record.get("country"), "company_db_record"
        )
        identity["sector"] = _dp(
            company_record.get("sector"), "company_db_record"
        )
        identity["source_tier"] = "T6_model_estimate"
        identity["is_mock"] = True
    else:
        identity["legal_name"] = _dp(None, "unavailable", "missing_data")
        identity["source_tier"] = "T6_model_estimate"
        identity["is_mock"] = True

    identity["human_review_required"] = True
    return identity


