class FinalReportGeneratorService:
    """
    Generates structured internal final report drafts.

    Entry points:
      generate_from_scorecard(db, scorecard_id)
      generate_from_candidate(db, candidate_id)
      generate_from_company(db, company_id)
      generate_from_report(db, report_id)
      validate_final_report(db, report_id)
      regenerate_report_section(db, report_id, section_name)

    All outputs are admin-only drafts with human_review_required=True.
    No BUY/SELL/HOLD/WATCH recommendations are ever produced.
    No price targets, fair values, or upside percentages are ever produced.
    """

    async def generate_from_scorecard(
        self, db: AsyncSession, scorecard_id: uuid.UUID
    ) -> FinalReportResponse:
        """Generate a final report draft from a scorecard record."""
        scorecard = await _load_scorecard_by_id(db, scorecard_id)
        if scorecard is None:
            raise ValueError(f"Scorecard {scorecard_id} not found.")

        candidate = None
        source_report = None
        company_record = None

        if scorecard.screening_candidate_id:
            candidate = await _load_candidate_by_id(
                db, scorecard.screening_candidate_id
            )

        if scorecard.report_id:
            source_report = await _load_report_by_id(db, scorecard.report_id)

        # Extract workflow state from the linked report if available
        state: dict[str, Any] = {}
        if source_report:
            state = _extract_workflow_state_from_report(source_report)

        citations: list[Citation] = []
        sources: list[Source] = []
        if source_report:
            citations = await _load_citations_for_report(db, source_report.id)
            sources = await _load_sources_for_citations(db, citations)

        return await self._generate_and_save(
            db=db,
            scorecard=scorecard,
            candidate=candidate,
            source_report=source_report,
            company_record=company_record,
            citations=citations,
            sources=sources,
            state=state,
        )

