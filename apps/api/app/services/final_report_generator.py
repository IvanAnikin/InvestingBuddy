async def _save_final_report_draft(
    db: AsyncSession,
    report_content: dict[str, Any],
    safety_result: SafetyValidationResult,
    schema_validation: dict[str, Any],
    source_summary: dict[str, Any],
    scorecard_id: uuid.UUID | None,
    company_name: str | None,
    ticker: str | None,
    source_report_id: uuid.UUID | None,
) -> Report:
    """
    Save the assembled final report as a new draft report record.
    """
    import json

    title_prefix = f"Final Report Draft — {company_name or 'Unknown'}"
    if ticker:
        title_prefix = f"Final Report Draft — {ticker} — {company_name or 'Unknown'}"

    slug = (
        f"final-report-{ticker or 'unknown'}-{uuid.uuid4().hex[:8]}"
        .lower()
        .replace(" ", "-")
    )

    # Build markdown summary for content_markdown
    content_lines = [
        "# INTERNAL ADMIN DRAFT — FINAL REPORT",
        "",
        "NOT INVESTMENT ADVICE. NOT A PUBLIC TRADING RECOMMENDATION. Human review required.",
        "",
        "---",
        "",
        "## Report Sections (Structured JSON — see safety_validation_json"
        " and schema_validation_json for validation status)",
        "",
        "```json",
        json.dumps(report_content, indent=2, default=str),
        "```",
    ]
    content_markdown = "\n".join(content_lines)

    schema_valid = schema_validation.get("is_valid", False)

    report = Report(
        id=uuid.uuid4(),
        title=title_prefix,
        slug=slug,
        report_type="company_deep_dive",
        status="draft",
        summary=(
            f"Phase 16 final report draft for {company_name or 'Unknown'}. "
            f"Safety: {'PASSED' if safety_result.passed else 'FAILED'}. "
            f"Schema: {'valid' if schema_valid else 'invalid'}. "
            f"Human review required."
        ),
        content_markdown=content_markdown,
        review_status="draft",
        human_review_required=True,
        final_report_version=FINAL_REPORT_VERSION,
        safety_validation_json=safety_result.model_dump(),
        schema_validation_json=schema_validation,
        source_summary_json=source_summary,
        scorecard_id=scorecard_id,
    )

    db.add(report)
    await db.flush()
    await db.refresh(report)
    return report


