    async def validate_final_report(
        self, db: AsyncSession, report_id: uuid.UUID
    ) -> FinalReportValidateResponse:
        """Re-validate an existing final report (safety + schema)."""
        report = await _load_report_by_id(db, report_id)
        if report is None:
            raise ValueError(f"Report {report_id} not found.")

        import json

        # Parse the content for safety + schema validation
        report_content: dict[str, Any] = {}
        if report.content_markdown:
            import re

            pattern = re.compile(r"```json\s*(.*?)\s*```", re.DOTALL)
            for match in pattern.finditer(report.content_markdown):
                try:
                    block = json.loads(match.group(1))
                    if isinstance(block, dict):
                        report_content = block
                        break
                except (json.JSONDecodeError, ValueError):
                    pass

        safety_result = run_safety_gate(report_content)

        # Schema validation against real-asset schema
        schema_result = validate_real_asset_report(report_content)
        schema_valid = schema_result.is_valid

        sections_present = [k for k in report_content if k in _REQUIRED_SECTIONS]
        missing_sections = [s for s in _REQUIRED_SECTIONS if s not in report_content]

        validation_warnings: list[str] = list(schema_result.warnings or [])
        if not safety_result.passed:
            validation_warnings.extend(safety_result.warnings)

        # Update the report's validation fields
        report.safety_validation_json = safety_result.model_dump()
        report.schema_validation_json = {
            "is_valid": schema_valid,
            "errors": list(schema_result.errors or []),
            "warnings": list(schema_result.warnings or []),
        }
        await db.flush()

        return FinalReportValidateResponse(
            report_id=report.id,
            schema_valid=schema_valid,
            safety_valid=safety_result.passed,
            human_review_required=True,
            safety_validation=safety_result,
            schema_validation_errors=list(schema_result.errors or []),
            schema_validation_warnings=list(schema_result.warnings or []),
            validation_warnings=validation_warnings,
            sections_present=sections_present,
            missing_sections=missing_sections,
        )

    async def regenerate_report_section(
        self, db: AsyncSession, report_id: uuid.UUID, section_name: str
    ) -> RegenerateSectionResponse:
        """Regenerate a single section of an existing final report."""
        if section_name not in _REQUIRED_SECTIONS:
            raise ValueError(
                f"Unknown section '{section_name}'. "
                f"Valid sections: {sorted(_REQUIRED_SECTIONS)}"
            )

        report = await _load_report_by_id(db, report_id)
        if report is None:
            raise ValueError(f"Report {report_id} not found.")

        # For deterministic regeneration, rebuild the section from workflow state
        state = _extract_workflow_state_from_report(report)
        scorecard: Scorecard | None = None
        if report.scorecard_id:
            scorecard = await _load_scorecard_by_id(db, report.scorecard_id)

        # Build only the requested section
        section_content: dict[str, Any] = {}
        match section_name:
            case "admin_disclaimer":
                section_content = _build_admin_disclaimer()
            case "executive_summary":
                section_content = _build_executive_summary(
                    None, None, scorecard,
                    state.get("committee_chair_summary"),
                    state.get("committee_chair_summary", {}).get(
                        "provisional_internal_status"
                    ) if state.get("committee_chair_summary") else None,
                )
            case "internal_scorecard":
                section_content = _build_internal_scorecard(scorecard)
            case "valuation_readiness":
                section_content = _build_valuation_readiness(
                    state.get("valuation_guard_summary"), scorecard
                )
            case "bull_case":
                section_content = _build_bull_case(state.get("bull_case_summary"))
            case "bear_case":
                section_content = _build_bear_case(state.get("bear_case_summary"))
            case "risk_analysis":
                section_content = _build_risk_analysis(state.get("risk_summary"))
            case "committee_chair_summary":
                section_content = _build_committee_chair_summary(
                    state.get("committee_chair_summary")
                )
            case "research_completeness_review":
                section_content = _build_research_completeness_review(
                    state.get("research_completeness_summary")
                )
            case _:
                section_content = {
                    "note": f"Section '{section_name}' requires full report regeneration.",
                    "provenance": "missing_data",
                }

        safety_result = run_safety_gate({section_name: section_content})

        warnings: list[str] = list(safety_result.warnings)
        if not safety_result.passed:
            warnings.append(
                f"Section '{section_name}' contains forbidden terms. "
                "Cannot update report section."
            )

        return RegenerateSectionResponse(
            report_id=report.id,
            section_name=section_name,
            regenerated=safety_result.passed,
            safety_valid=safety_result.passed,
            warnings=warnings,
        )

    # ── Core private assembly ─────────────────────────────────────────────────

    async def _generate_and_save(
        self,
        db: AsyncSession,
        scorecard: Scorecard | None,
        candidate: ScreeningCandidate | None,
        source_report: Report | None,
        company_record: dict[str, Any] | None,
        citations: list[Citation],
        sources: list[Source],
        state: dict[str, Any],
    ) -> FinalReportResponse:
        company_snapshot = state.get("company_snapshot")
        financial_data_summary = state.get("financial_data_summary")
        source_quality_summary = state.get("source_quality_summary")
        research_completeness_summary = state.get("research_completeness_summary")
        upgraded_citation_validation = state.get("upgraded_citation_validation")
        bull_case_summary = state.get("bull_case_summary")
        bear_case_summary = state.get("bear_case_summary")
        risk_summary = state.get("risk_summary")
        valuation_guard_summary = state.get("valuation_guard_summary")
        committee_chair_summary = state.get("committee_chair_summary")
        fundamentals_data = state.get("fundamentals_data")
        fundamentals_available = state.get("fundamentals_available")
        schema_validation_result = state.get("schema_validation_result")
        source_tier = state.get("source_tier") or (
            company_snapshot.get("source_tier") if company_snapshot else None
        )

        schema_valid = (
            schema_validation_result.get("is_valid", False)
            if schema_validation_result
            else False
        )
        human_review_required = True

        report_content = _assemble_final_report_content(
            company_snapshot=company_snapshot,
            company_record=company_record,
            candidate=candidate,
            scorecard=scorecard,
            financial_data_summary=financial_data_summary,
            source_quality_summary=source_quality_summary,
            research_completeness_summary=research_completeness_summary,
            upgraded_citation_validation=upgraded_citation_validation,
            bull_case_summary=bull_case_summary,
            bear_case_summary=bear_case_summary,
            risk_summary=risk_summary,
            valuation_guard_summary=valuation_guard_summary,
            committee_chair_summary=committee_chair_summary,
            fundamentals_data=fundamentals_data,
            fundamentals_available=fundamentals_available,
            source_tier=source_tier,
            sources=sources,
            citations=citations,
            report=source_report,
            agent_run_id=str(source_report.created_by_agent_run_id)
            if source_report and source_report.created_by_agent_run_id
            else None,
            schema_valid=schema_valid,
            human_review_required=human_review_required,
        )

        # Run safety gate
        safety_result = run_safety_gate(report_content)

        # Update checklist with actual safety result
        checklist = report_content.get("human_review_checklist", [])
        if checklist and isinstance(checklist[0], dict):
            checklist[0]["completed"] = safety_result.passed
            if not safety_result.passed:
                checklist[0]["note"] = (
                    "BLOCKED: forbidden terms found. Revise report."
                )

        # Schema validation
        schema_check = validate_real_asset_report(report_content)
        schema_validation_for_save = {
            "is_valid": schema_check.is_valid,
            "errors": list(schema_check.errors or []),
            "warnings": list(schema_check.warnings or []),
        }

        # Source summary
        source_summary_for_save = {
            "total_sources": len(sources),
            "total_citations": len(citations),
            "source_types": list({s.source_type for s in sources}),
        }

        # Derive company_name + ticker for title
        company_name: str | None = None
        ticker: str | None = None
        if company_snapshot:
            ci = company_snapshot.get("company_identity", {})
            company_name = ci.get("legal_name")
            ticker = ci.get("ticker")
        elif company_record:
            company_name = company_record.get("name")
            ticker = company_record.get("ticker")
        elif candidate:
            company_name = candidate.name
            ticker = candidate.ticker

        saved_report = await _save_final_report_draft(
            db=db,
            report_content=report_content,
            safety_result=safety_result,
            schema_validation=schema_validation_for_save,
            source_summary=source_summary_for_save,
            scorecard_id=scorecard.id if scorecard else None,
            company_name=company_name,
            ticker=ticker,
            source_report_id=source_report.id if source_report else None,
        )

        sections_generated = list(report_content.keys())
        missing_sections = [s for s in _REQUIRED_SECTIONS if s not in report_content]

        # Build human review checklist items for response
        missing_count_val = report_content.get("missing_information", {}).get(
            "total_missing_items", 0
        )
        is_mock_val = True
        if company_snapshot:
            is_mock_val = company_snapshot.get("is_mock", True)

        checklist_items = _build_human_review_checklist(
            safety_valid=safety_result.passed,
            schema_valid=schema_check.is_valid,
            has_scorecard=scorecard is not None,
            has_bull_bear=bull_case_summary is not None
            and bear_case_summary is not None,
            has_risk=risk_summary is not None,
            has_citations=len(citations) > 0,
            missing_count=missing_count_val,
            is_mock=is_mock_val,
        )

        provisional_status = None
        if committee_chair_summary:
            provisional_status = committee_chair_summary.get(
                "provisional_internal_status"
            )
        if not provisional_status and scorecard:
            provisional_status = scorecard.internal_status
        if not provisional_status or provisional_status not in ALLOWED_INTERNAL_STATUSES:
            provisional_status = "not_enough_data"

        validation_warnings: list[str] = list(schema_check.warnings or [])
        if not safety_result.passed:
            validation_warnings.extend(safety_result.warnings)

        return FinalReportResponse(
            report_id=saved_report.id,
            status="draft",
            review_status="draft",
            schema_valid=schema_check.is_valid,
            safety_valid=safety_result.passed,
            human_review_required=True,
            internal_status=provisional_status,
            sections_generated=sections_generated,
            missing_sections=missing_sections,
            safety_validation=safety_result,
            schema_validation_errors=list(schema_check.errors or []),
            schema_validation_warnings=list(schema_check.warnings or []),
            validation_warnings=validation_warnings,
            scorecard_id=scorecard.id if scorecard else None,
            source_count=len(sources),
            citation_count=len(citations),
            human_review_checklist=checklist_items,
        )
